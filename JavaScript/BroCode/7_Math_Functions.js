let x = 3.14;
let y = 5;
let z = 9;
let maximum;
let minimum;

x = Math.round(x);
x = Math.floor(x); // Floor will always round down
x = Math.ceil(x); // Ceil will always round up
x = Math.pow(x, 2); // X to the power of 2, 3, etc.
x = Math.sqrt(x); // Square root
x = Math.abs(x); // Absolute value (distance away from 0)
maximum = Math.max(x, y, z);
minimum = Math.min(x, y, z);
x = Math.PI; // Displays pi to a greater detail

console.log(x);